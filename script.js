// Função verificarPar
function verificarPar(numero) {
    if (numero % 2 === 0) {
      return "Par";
    } else {
      return "Ímpar";
    }
  }
  
  // Exemplos de uso
  console.log(verificarPar(4)); // Resultado: "Par"
  console.log(verificarPar(7)); // Resultado: "Ímpar"
  console.log(verificarPar(0)); // Resultado: "Par"
  console.log(verificarPar(-5)); // Resultado: "Ímpar"
  
  
