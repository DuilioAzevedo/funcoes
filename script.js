// Função saudacao
function saudacao(nome = "Visitante") {
  return `Olá, ${nome}! Seja bem-vindo(a)!`;
}

// Exemplos de uso
console.log(saudacao("Carlos")); // Resultado: "Olá, Carlos! Seja bem-vindo(a)!"
console.log(saudacao("Ana")); // Resultado: "Olá, Ana! Seja bem-vindo(a)!"
console.log(saudacao()); // Resultado: "Olá, Visitante! Seja bem-vindo(a)!"
  
  
