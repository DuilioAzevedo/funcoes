// Função maiorNumero
function maiorNumero(num1, num2, num3) {
  return Math.max(num1, num2, num3);
}

// Exemplos de uso
console.log(maiorNumero(10, 25, 15)); // Resultado: 25
console.log(maiorNumero(7, 3, 9)); // Resultado: 9
console.log(maiorNumero(-5, -1, -10)); // Resultado: -1
console.log(maiorNumero(100, 200, 150)); // Resultado: 200
